// ---------- Основная функция получения случайного приветствия ----------
export function getRandomGreeting(fullName: string, date: Date): string {
  // 1. Разбираем ФИО
  const { lastName, firstName, patronymic } = parseFullName(fullName);

  // 2. Определяем день недели (0 = вс, 1 = пн, ..., 6 = сб)
  const weekday = date.getDay(); // 0..6

  // 3. Случайный выбор типа: 0 – общая фраза, 1 – фраза дня
  const type = Math.random() < 0.5 ? 'general' : 'weekday';

  let selectedTemplate = '';
  if (type === 'general') {
    const randomIndex = Math.floor(Math.random() * generalGreetings.length);
    selectedTemplate = generalGreetings[randomIndex];
  } else {
    const dayPhrases = weekdayGreetings[weekday];
    const randomIndex = Math.floor(Math.random() * dayPhrases.length);
    selectedTemplate = dayPhrases[randomIndex];
  }

  // 4. Замена плейсхолдеров
  let result = selectedTemplate
    .replace(/{Имя}/g, firstName || lastName)   // если нет имени, используем фамилию
    .replace(/{Фамилия}/g, lastName)
    .replace(/{Отчество}/g, patronymic || '')
    .replace(/{ФИО}/g, fullName);

  // Если остались лишние пробелы из-за пустого отчества, подчищаем
  result = result.replace(/\s+/g, ' ').trim();

  return result;
}
