// ---------- Парсер ФИО ----------
interface ParsedName {
  lastName: string;
  firstName: string;
  patronymic: string;
}

export function parseFullName(fullName: string): ParsedName {
  const parts = fullName.trim().split(/\s+/);
  if (parts.length === 3) {
    return { lastName: parts[0], firstName: parts[1], patronymic: parts[2] };
  } else if (parts.length === 2) {
    return { lastName: parts[0], firstName: parts[1], patronymic: '' };
  } else {
    // Если только одно слово или пусто
    return { lastName: parts[0] || '', firstName: parts[0] || '', patronymic: '' };
  }
}

